
#sviluppo le mie funzioni
#esempio. funzione somma. un afunzione nuova si usa la funzione function(). definisco gli argomenti della funzione
#devo aprire le parentesi {}
#return() funzione per chiudere la funzione

somma <-function(x,y){
  z=x+y
  return(z)
}

#ora posso usare la funzione somma!

somma(1,100)

#creo la funizone differenza

differenza <-function(x,y){
  z=x-y
  return(z)
}

differenza(8,12)

